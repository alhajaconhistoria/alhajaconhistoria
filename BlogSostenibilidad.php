    <h1>La Ropa Pre-Amada: Valorando su Historia</h1>
    <br>
    <img src="wendybanner.png" alt="png" style="position: relative;">
    <p>Mi historia con la ropa pre-amada ya tiene 8 años, esta no llega con nada relacionado al medio ambiente o incluso con la conciencia social. Esta historia llegó en un momento de obstáculos en mi vida, en el que económicamente andaba ajustada: mi esposo había perdido el empleo, junto a ello nos acompañaban deudas que costear por un accidente que tuvo él anteriormente. Rápidamente las costumbres cambiaron; tuvimos que hacer algunos sacrificios y priorizar en su mayoría lo esencial. Adaptarme no fue fácil, siempre fui una persona que le gustaba comprar, amante de la moda, en ocasiones incluso por impulso, sin reflexionar en el uso que le iba a dar a ese producto o prenda. Siempre he pensado que de los momentos difíciles se aprende algo y si esta situación de limitación me llevó a revisar lo que tenía, como buscar una y otra manera de usar una prenda (descubrí que podía ser creativa), también me hizo darme cuenta que tenía ropa que no usaba. Recuerdo preguntarme, ¿Y ahora qué hago con ella? Esto me llevó a indagar, averiguar y descubrir que existían en mi país tiendas de intercambio, lo me llevó a entender que había otras formas de adquirir prendas de una manera más económica, especial y sobre todo consciente.</p>
    <p class="texto">Una de las cosas que valoré al adquirir ropa de segunda, es que al ser única (no tiene tallas ni modelos similares), tengo que pensar y repensar si esa prenda es conveniente para mí en todos los sentidos. Esto se alejaba mucho al comprar por comprar, ya que cuando adquiría una prenda con historia estaba decidiéndome por ella, ya sea por alguna característica especial que reflejara lo que soy yo, y encontrar ello no es fácil, por ello cuando encuentro una pieza que es para mí es como encontrar un tesoro. El tiempo me llevó a formarme, a aprender más sobre sostenibilidad y a valorar la reutilización. Darle una vida más larga a las prendas, hacerlas más funcionales, validar el repetir de atuendo, usar tu creatividad para hacer más combinaciones con una prenda, darle una oportunidad a la ropa de segunda vida y verlas como piezas con conciencia, todo eso nos llama a un mundo mejor. </p>
    <p class="texto">Es por ello que la ropa de segunda tiene un lugar muy especial en mi corazón, con una historia junto a ella que me hace valorarla aún más.</p>
